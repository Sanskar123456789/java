public class Student {
	private String id;
	private String name;
	private String email;
	
	public void setid(String id) {
		this.id =id;
	}
	public void setname(String name) {
		this.name =name;
	}
	public void setemail(String email) {
		this.email =email;
	}
	
	public String getid() {
		return this.id;
	}
	public String getname() {
		return this.name;
	}
	public String getemail() {
		return this.email;
	}
}
